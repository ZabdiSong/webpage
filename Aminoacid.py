# ----------------------------
# QUESTION FLOW
# ----------------------------
if st.session_state.q < len(questions):

    q_text, options = questions[st.session_state.q]

    st.subheader(f"Q{st.session_state.q + 1}")
    st.write(q_text)

    for text, aas in options:
        if st.button(text):
            for a in aas:
                st.session_state.scores[a] += 1
            st.session_state.q += 1
            st.rerun()

# ----------------------------
# RESULT
# ----------------------------
else:
    result = max(st.session_state.scores, key=st.session_state.scores.get)
    data = amino_acids[result]

    st.subheader("🧬 Your Amino Acid Personality")

    st.markdown(f"### {data['name']}")

    st.write(data["desc_cn"])

    if st.button("Restart / 重新测试"):
        st.session_state.q = 0
        st.session_state.scores = {k: 0 for k in amino_acids}
        st.rerun()
